/**
 * 获取所有行业列表
 */
export const getIndustries = (): string[] => {
  return Object.keys(jobCategories);
};

/**
 * 获取指定行业的所有分类
 */
export const getCategories = (industry: string): string[] => {
  if (!industry || !jobCategories[industry]) return [];
  return Object.keys(jobCategories[industry]);
};

/**
 * 获取指定行业和分类下的所有职位
 */
export const getPositions = (industry: string, category: string): string[] => {
  if (!industry || !category || !jobCategories[industry]?.[category]) return [];
  return jobCategories[industry][category];
};

/**
 * 搜索职位（模糊匹配）
 */
export const searchPositions = (keyword: string): Array<{
  industry: string;
  category: string;
  position: string;
}> => {
  const results: Array<{ industry: string; category: string; position: string; }> = [];
  
  if (!keyword.trim()) return results;
  
  const searchTerm = keyword.toLowerCase();
  
  Object.entries(jobCategories).forEach(([industry, categories]) => {
    Object.entries(categories).forEach(([category, positions]) => {
      positions.forEach(position => {
        if (position.toLowerCase().includes(searchTerm)) {
          results.push({ industry, category, position });
        }
      });
    });
  });
  
  return results;
};

/**
 * 验证职位路径是否有效
 */
export const validateJobPath = (industry: string, category: string, position: string): boolean => {
  return jobCategories[industry]?.[category]?.includes(position) || false;
};

/**
 * 获取职位的完整路径描述
 */
export const getJobPathDescription = (industry: string, category: string, position: string): string => {
  if (!validateJobPath(industry, category, position)) {
    return '无效的职位路径';
  }
  return `${industry} > ${category} > ${position}`;
};
